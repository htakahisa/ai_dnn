def parse_maze(s):
    lines = [line.strip() for line in s.strip("\n").split("\n") if line.strip()]
    return lines

rows = parse_maze(maze_str)
height = len(rows)
width = len(rows[0])
print(f"height={height}, width={width}")

cell_size = 18
colors = {
    "1": "#34495e",  # 壁
    "0": "#ffffff",  # 通路
    "2": "#f1c40f",  # ゴール候補
}

root = tk.Tk()
root.title("Maze Preview")
canvas = tk.Canvas(root, width=width * cell_size, height=height * cell_size)
canvas.pack()

for r in range(height):
    for c in range(width):
        ch = rows[r][c]
        color = colors.get(ch, "#ff00ff")  # 想定外の文字はマゼンタで目立たせる
        x1, y1 = c * cell_size, r * cell_size
        x2, y2 = x1 + cell_size, y1 + cell_size
        canvas.create_rectangle(x1, y1, x2, y2, fill=color, outline="#eee")

root.mainloop()