def view_map_direct(maze_str):
    # 行ごとに分割し、空行を除外
    lines = [line.strip() for line in maze_str.strip("\n").split("\n") if line.strip()]
    
    if not lines:
        print("エラー: マップデータが空です。")
        return

    height = len(lines)
    width = len(lines[0])
    
    print(f"--- マップ形状の検出結果 ---")
    print(f"検出された高さ（行数）: {height}")
    print(f"検出された横幅（列数）: {width}")
    print(f"----------------------------")

    # 2次元配列化
    grid = np.array([[int(ch) for ch in line] for line in lines], dtype=np.int32)

    # Tkinterで描画
    root = tk.Tk()
    root.title(f"Map Viewer ({width} x {height})")
    
    # 画面サイズに合わせてマス目の大きさを調整してください
    cell_size = 12 
    
    canvas = tk.Canvas(root, width=width * cell_size, height=height * cell_size)
    canvas.pack()

    color_map = {
        0: "white",     # 通路
        1: "#34495e",   # 壁
        2: "#F1DE94",   # ゴール候補（黄色）
        3: "#3498db",   # 3（青）
        4: "#9b59b6",   # 4（紫）
    }

    for r in range(height):
        for c in range(width):
            val = grid[r, c]
            color = color_map.get(val, "orange")
            
            x1, y1 = c * cell_size, r * cell_size
            x2, y2 = x1 + cell_size, y1 + cell_size
            canvas.create_rectangle(x1, y1, x2, y2, fill=color, outline="#ddd")

    root.mainloop()

if __name__ == "__main__":
    view_map_direct(NEW_MAZE_STR)